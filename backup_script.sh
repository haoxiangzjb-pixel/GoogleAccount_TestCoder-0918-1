#!/bin/bash

# Скрипт для создания резервной копии файла
# Использование: ./backup_script.sh <имя_файла>

# Проверяем, передан ли аргумент
if [ $# -eq 0 ]; then
    echo "Ошибка: Не указан файл для резервного копирования"
    echo "Использование: $0 <имя_файла>"
    exit 1
fi

# Получаем имя файла из аргумента
SOURCE_FILE="$1"

# Проверяем, существует ли файл
if [ ! -f "$SOURCE_FILE" ]; then
    echo "Ошибка: Файл '$SOURCE_FILE' не существует"
    exit 1
fi

# Создаем имя для резервной копии с временной меткой
TIMESTAMP=$(date +"%Y%m%d_%H%M%S")
BACKUP_NAME="${SOURCE_FILE}_backup_${TIMESTAMP}"

# Создаем резервную копию
cp "$SOURCE_FILE" "$BACKUP_NAME"

# Проверяем успешность операции
if [ $? -eq 0 ]; then
    echo "Резервная копия создана: $BACKUP_NAME"
else
    echo "Ошибка при создании резервной копии"
    exit 1
fi