# Скрипт R для построения диаграммы рассеяния и сохранения в файл со случайным именем

# Установка случайного seed для воспроизводимости (опционально)
set.seed(Sys.time())

# Генерация случайных данных для диаграммы рассеяния
n <- 100  # количество точек
x <- rnorm(n)
y <- rnorm(n)

# Создание случайного имени файла
random_name <- paste0("scatter_plot_", sample(10000:99999, 1), ".png")

# Создание диаграммы рассеяния
png(random_name, width = 800, height = 600)
plot(x, y, 
     main = "Диаграмма рассеяния", 
     xlab = "X значения", 
     ylab = "Y значения",
     pch = 19,  # тип точки
     col = "blue"  # цвет точек
)
abline(lm(y ~ x), col = "red")  # добавление линии регрессии
dev.off()  # закрытие устройства вывода

cat("Диаграмма рассеяния сохранена в файл:", random_name, "\n")