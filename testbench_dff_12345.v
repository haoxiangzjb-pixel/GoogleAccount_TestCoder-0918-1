// Тестовая плата для D-триггера
`timescale 1ns/1ps

module testbench_dff;
    reg clk;
    reg rst_n;
    reg d;
    wire q;

    // Подключение тестируемого модуля (предполагаем, что он называется dff)
    dff uut (
        .clk(clk),
        .rst_n(rst_n),
        .d(d),
        .q(q)
    );

    // Генерация тактового сигнала (период 10 нс)
    initial begin
        clk = 0;
        forever #5 clk = ~clk; // переключение каждые 5 нс для получения 10 нс периода
    end

    // Основной процесс стимулирования
    initial begin
        // Инициализация сигналов
        d = 0;
        rst_n = 0; // активный уровень сброса (предполагается низкий уровень)

        // Сброс
        #15; // подождать 15 нс
        rst_n = 1; // деактивировать сброс

        // Тестирование
        #10 d = 1; // подать 1 на D
        #20 d = 0; // подать 0 на D
        #20 d = 1; // подать 1 на D
        #20 d = 0; // подать 0 на D

        // Завершение симуляции
        #10 $finish;
    end

    // Мониторинг сигналов
    initial begin
        $monitor("Time: %0t ns, CLK: %b, RST_N: %b, D: %b, Q: %b", $time, clk, rst_n, d, q);
    end

endmodule