# Скрипт R для построения диаграммы рассеяния и сохранения в файл

# Установка случайных данных
set.seed(123)  # Для воспроизводимости
x <- rnorm(50)
y <- rnorm(50)

# Создание диаграммы рассеяния
plot(x, y, 
     main = "Диаграмма рассеяния", 
     xlab = "X значения", 
     ylab = "Y значения",
     pch = 19,  # Тип точки
     col = "blue"  # Цвет точек
)

# Генерация случайного имени для выходного файла
random_name <- paste0("scatter_plot_", sample(10000:99999, 1), ".png")

# Сохранение диаграммы в файл
png(random_name)
plot(x, y, 
     main = "Диаграмма рассеяния", 
     xlab = "X значения", 
     ylab = "Y значения",
     pch = 19, 
     col = "blue"
)
dev.off()

cat("Диаграмма рассеяния сохранена в файл:", random_name, "\n")