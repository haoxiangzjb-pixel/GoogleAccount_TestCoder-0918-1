// Тестовая плата для D-триггера
module tb_d_flip_flop;
    // Объявление сигналов
    reg clk;
    reg reset;
    reg d;
    wire q;

    // Инстанцирование тестируемого модуля (D-триггер)
    // Предполагается, что модуль называется d_flip_flop
    d_flip_flop uut (
        .clk(clk),
        .reset(reset),
        .d(d),
        .q(q)
    );

    // Генерация тактового сигнала (например, период 10 единиц времени)
    initial begin
        clk = 0;
        forever #5 clk = ~clk; // Инвертируем clk каждые 5 единиц времени
    end

    // Основной процесс тестирования
    initial begin
        // Инициализация сигналов
        reset = 1;
        d = 0;
        #10; // Ждем 10 единиц времени

        // Снимаем сброс и тестируем
        reset = 0;
        #5; // Ждем следующего фронта тактового сигнала

        // Тестируем переключение выхода
        d = 1;
        #10; // Ждем 2 такта
        d = 0;
        #10; // Ждем 2 такта
        d = 1;
        #10; // Ждем 2 такта
        d = 0;
        #10; // Ждем 2 такта

        // Завершаем симуляцию
        $finish;
    end

endmodule