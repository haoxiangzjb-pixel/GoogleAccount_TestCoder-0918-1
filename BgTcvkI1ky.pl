% База фактов Prolog о семье
% Отношения: родитель, муж, жена, ребенок, брат, сестра

% Мужчины
мужчина(иван).
мужчина(петр).
мужчина(сергей).
мужчина(андрей).
мужчина(михаил).

% Женщины
женщина(мария).
женщина(ольга).
женщина(анна).
женщина(екатерина).
женщина(татьяна).

% Браки
муж(иван, мария).
муж(петр, ольга).
муж(сергей, анна).

жена(мария, иван).
жена(ольга, петр).
жена(анна, сергей).

% Родительские отношения
родитель(иван, андрей).    % Иван - отец Андрея
родитель(иван, екатерина). % Иван - отец Екатерины
родитель(мария, андрей).   % Мария - мать Андрея
родитель(мария, екатерина). % Мария - мать Екатерины

родитель(петр, татьяна).   % Петр - отец Татьяны
родитель(ольга, татьяна).  % Ольга - мать Татьяны

родитель(сергей, михаил).  % Сергей - отец Михаила
родитель(анна, михаил).    % Анна - мать Михаила

% Правила для определения детей
ребенок(X, Y) :- родитель(Y, X).

% Правила для определения братьев и сестер
брат(X, Y) :- мужчина(X), родитель(Z, X), родитель(Z, Y), X \= Y.
сестра(X, Y) :- женщина(X), родитель(Z, X), родитель(Z, Y), X \= Y.

% Дедушки и бабушки
дедушка(X, Z) :- мужчина(X), родитель(X, Y), родитель(Y, Z).
бабушка(X, Z) :- женщина(X), родитель(X, Y), родитель(Y, Z).