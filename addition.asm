section .text
    global _start

; Функция сложения двух 32-битных целых чисел
; Вход: eax = первое число, ebx = второе число
; Выход: eax = результат сложения
add_func:
    add eax, ebx
    ret

; Точка входа в программу
_start:
    ; Загружаем числа для сложения в регистры eax и ebx
    mov eax, 5
    mov ebx, 10

    ; Вызываем функцию сложения
    call add_func

    ; Результат теперь в eax (15)

    ; Выход из программы
    ; Загружаем системный вызов exit в eax (1)
    ; и код возврата из нашей программы в ebx (результат сложения)
    mov eax, 1      ; sys_exit
    int 0x80        ; вызываем ядро