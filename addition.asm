; Функция на ассемблере x86 для сложения двух 32-битных целых чисел
; Соглашение о вызовах: первый аргумент в EAX, второй в EBX, результат в EAX

section .text
    global _start

_start:
    ; Пример: сложим 5 и 10
    mov eax, 5      ; Загружаем первое число в EAX
    mov ebx, 10     ; Загружаем второе число в EBX
    add eax, ebx    ; Складываем EBX с EAX, результат в EAX

    ; Завершаем программу, результат в EAX (для отладки)
    ; В реальной функции, вызываемой из другого кода, просто ret
    ; mov ebx, eax  ; Перемещаем результат в EBX для системного вызова exit
    ; mov eax, 1    ; Номер системного вызова sys_exit
    ; int 0x80      ; Вызов ядра

    ; Для функции, вызываемой извне:
    ret             ; Возвращаемся, результат уже в EAX