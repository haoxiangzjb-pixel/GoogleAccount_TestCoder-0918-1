section .text
    global _start

_start:
    ; Загружаем два числа в регистры
    mov eax, 5      ; Первое число
    mov ebx, 3      ; Второе число

    ; Складываем числа
    add eax, ebx    ; Результат сохраняется в eax

    ; Выход из программы
    ; mov ecx, eax  ; Если нужно использовать результат, например, для кода выхода
    mov eax, 1      ; Системный вызов sys_exit
    int 0x80        ; Вызов ядра