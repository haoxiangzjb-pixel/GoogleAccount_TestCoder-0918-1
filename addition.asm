section .text
    global _start

; Функция сложения двух чисел
; Вход: EAX = первое число, EBX = второе число
; Выход: EAX = результат сложения
add_numbers:
    add eax, ebx        ; Складываем EAX и EBX, результат в EAX
    ret                 ; Возвращаемся из функции

_start:
    ; Пример использования функции
    mov eax, 53         ; Загружаем первое число в EAX
    mov ebx, 27         ; Загружаем второе число в EBX
    call add_numbers    ; Вызываем функцию сложения

    ; Завершаем программу (Linux)
    mov ebx, 0          ; Код возврата
    mov eax, 1          ; Системный вызов sys_exit
    int 0x80            ; Вызов ядра