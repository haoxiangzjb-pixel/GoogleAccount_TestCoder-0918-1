section .text
    global _start

; Функция сложения двух чисел
; Вход: rdi - первое число, rsi - второе число
; Выход: rax - результат сложения
add_numbers:
    mov rax, rdi    ; Копируем первое число в rax
    add rax, rsi    ; Прибавляем второе число
    ret             ; Возвращаем результат в rax

_start:
    ; Пример использования функции
    mov rdi, 5      ; Загружаем первое число (например, 5)
    mov rsi, 3      ; Загружаем второе число (например, 3)
    call add_numbers ; Вызываем функцию сложения

    ; Выход из программы
    ; rax уже содержит результат сложения (8)
    mov rdi, rax    ; Перемещаем результат в rdi для системного вызова exit
    mov rax, 60     ; Номер системного вызова exit
    syscall         ; Вызываем системный вызов