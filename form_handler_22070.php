<?php
// Проверяем, был ли запрос отправлен методом POST
if ($_SERVER["REQUEST_METHOD"] == "POST") {
    // Получаем данные из формы
    $name = $_POST['name'] ?? '';
    $email = $_POST['email'] ?? '';

    // Простая валидация (проверка на пустоту)
    if (!empty($name) && !empty($email)) {
        // Открываем файл для записи (в режиме добавления)
        $file = fopen("form_data.txt", "a");

        // Форматируем строку для записи
        $data = "Имя: " . $name . ", Email: " . $email . "\n";

        // Записываем данные в файл
        fwrite($file, $data);

        // Закрываем файл
        fclose($file);

        // Выводим сообщение об успешной обработке
        echo "Данные успешно сохранены!";
    } else {
        // Выводим сообщение об ошибке, если поля пусты
        echo "Пожалуйста, заполните все поля формы.";
    }
} else {
    // Выводим сообщение, если запрос не POST
    echo "Данные можно отправить только методом POST.";
}
?>