# array_operations.jl
# This script demonstrates basic array operations in Julia.

# Define an example array
arr = [1, 2, 3, 4, 5]

# Calculate the sum
arr_sum = sum(arr)
println("Sum of the array: ", arr_sum)

# Calculate the mean
arr_mean = mean(arr)
println("Mean of the array: ", arr_mean)

# Calculate the maximum value
arr_max = maximum(arr)
println("Maximum value in the array: ", arr_max)

# Calculate the minimum value
arr_min = minimum(arr)
println("Minimum value in the array: ", arr_min)
