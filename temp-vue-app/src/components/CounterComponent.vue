<template>
  <div>
    <button @click="increment">{{ buttonText }}</button>
    <p>Count: {{ count }}</p>
  </div>
</template>

<script>
export default {
  name: 'CounterComponent',
  data() {
    return {
      count: 0,
    };
  },
  computed: {
    buttonText() {
      return `Clicked ${this.count} times`;
    }
  },
  methods: {
    increment() {
      this.count++;
    }
  }
};
</script>

<style scoped>
button {
  padding: 10px 20px;
  font-size: 16px;
  cursor: pointer;
}
p {
  margin-top: 10px;
  font-size: 18px;
}
</style>