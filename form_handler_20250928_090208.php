<?php
// Проверяем, был ли запрос отправлен методом POST
if ($_SERVER["REQUEST_METHOD"] == "POST") {
    // Здесь можно добавить логику для обработки данных формы
    // Например, валидация, очистка и сохранение в файл или базу данных

    // Пример: сохранение данных в файл
    $name = $_POST['name'] ?? 'Не указано';
    $email = $_POST['email'] ?? 'Не указано';

    $data = "Имя: " . $name . ", Email: " . $email . "\n";
    file_put_contents('form_data.txt', $data, FILE_APPEND | LOCK_EX);

    echo "Спасибо, данные получены и сохранены!";
} else {
    // Если форма не отправлена, можно вывести сообщение или перенаправить
    echo "Данные формы не были отправлены методом POST.";
}
?>