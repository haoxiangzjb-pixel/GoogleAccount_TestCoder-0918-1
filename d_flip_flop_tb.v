`timescale 1ns / 1ps

module d_flip_flop_tb;

    // Объявление сигналов
    reg clk;
    reg reset_n;
    reg d;
    wire q;

    // Инициализация модуля D-триггера
    d_flip_flop uut (
        .clk(clk),
        .reset_n(reset_n),
        .d(d),
        .q(q)
    );

    // Генерация тактового сигнала (период 10 нс)
    initial begin
        clk = 0;
        forever #5 clk = ~clk; // Переключение каждые 5 нс, период 10 нс
    end

    // Основной блок стимулов
    initial begin
        // Инициализация сигналов
        reset_n = 0; // Активный уровень сброса
        d = 0;

        // Сброс
        #15; // Ждем 15 нс
        reset_n = 1; // Сброс завершен
        #20; // Ждем 20 нс после сброса

        // Подача тестовых данных
        d = 1; // Установить D в 1
        #20;  // Ждем 20 нс (2 такта)
        d = 0; // Установить D в 0
        #20;  // Ждем 20 нс (2 такта)
        d = 1; // Установить D в 1
        #20;  // Ждем 20 нс (2 такта)

        // Завершение моделирования
        #20;
        $finish;
    end

    // Отслеживание сигналов
    initial begin
        $monitor("Time: %0t ns, Reset_N: %b, D: %b, Q: %b", $time, reset_n, d, q);
    end

endmodule