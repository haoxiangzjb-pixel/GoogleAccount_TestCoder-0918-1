section .text
    global _start

; Функция сложения двух 32-битных целых чисел
; Вход:
;   eax - первое число
;   ebx - второе число
; Выход:
;   eax - результат сложения
add_func:
    add eax, ebx
    ret

; Пример использования функции
_start:
    mov eax, 5      ; Загрузить первое число в eax
    mov ebx, 3      ; Загрузить второе число в ebx
    call add_func   ; Вызвать функцию сложения

    ; Результат теперь в eax (в данном случае, 8)

    ; Выход из программы
    mov ebx, 0      ; код возврата
    mov eax, 1      ; системный вызов sys_exit
    int 0x80        ; вызвать прерывание ядра