section .text
    global _start

; Функция сложения двух 32-битных целых чисел
; Вход: EAX = первое число, EBX = второе число
; Выход: EAX = результат сложения
add_func:
    add eax, ebx
    ret

_start:
    ; Пример использования функции
    mov eax, 5      ; Загрузить первое число в EAX
    mov ebx, 10     ; Загрузить второе число в EBX
    call add_func   ; Вызвать функцию сложения

    ; Результат теперь в EAX (ожидаем 15)

    ; Выход из программы
    mov ebx, 0      ; Код возврата
    mov eax, 1      ; Системный вызов sys_exit
    int 0x80        ; Вызов ядра