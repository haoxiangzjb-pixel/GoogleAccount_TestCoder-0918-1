// Функция для сложения двух чисел
function add(a, b) {
  return a + b;
}

// Экспорт функции для возможного использования в других файлах
// export default add; // Можно раскомментировать, если используется модульная система

// Пример использования
console.log(add(2, 3)); // Выведет 5
console.log(add(-1, 5)); // Выведет 4